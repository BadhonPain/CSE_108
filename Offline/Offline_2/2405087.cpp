#include <iostream>
using namespace std;

class PartyMember
{
    int member_id;
    string name;
    long asset_value;
    string nominated_for;

public:
    PartyMember()
    {
        member_id = -1;
        name = "";
        asset_value = 0;
        nominated_for = "";
    }

    PartyMember(string n, long asset)
    {
        member_id = -1;
        name = n;
        asset_value = asset;
        nominated_for = "";
    }
    PartyMember(const PartyMember &pm)
    {
        member_id = pm.member_id;
        name = pm.name;
        asset_value = pm.asset_value;
        nominated_for = pm.nominated_for;
    }
    void setID(int id)
    {
        member_id = id;
    }
    void setName(string n)
    {
        name = n;
    }
    void setAsset(long asset)
    {
        asset_value = asset;
    }
    void setConstituency(string place)
    {
        nominated_for = place;
    }

    int getID()
    {
        return member_id;
    }
    string getName()
    {
        return name;
    }
    long getAsset()
    {
        return asset_value;
    }
    string getConstituency()
    {
        return nominated_for;
    }

    void show_member_details()
    {
        if (nominated_for != "")
            cout << "ID: " << member_id << ", Name: " << name << ", Asset: " << asset_value << ", Constituency: " << nominated_for
                 << endl;
        else
            cout << "ID: " << member_id << ", Name: " << name << ", Asset: " << asset_value << endl;
    }
};

class PoliticalParty
{
    string name;
    PartyMember *members[1000];
    int member_count;
    int validID = -1;

public:
    PoliticalParty(const PoliticalParty &pp) 
    {
        name = pp.name;
        member_count = pp.member_count;
        validID = pp.validID; 
        for (int i = 0; i < member_count; i++)
        {
            members[i] = new PartyMember(*pp.members[i]);
        }
    }

    PoliticalParty(string party_name)
    {
        name = party_name;
        member_count = 0;
        validID = 1;
    }

    ~PoliticalParty()
    {
        for (int i = 0; i < member_count; i++)
            delete members[i];
    }

    PartyMember search_member(string constituency)
    {

        for (int i = 0; i < member_count; i++)
        {
            if (members[i]->getConstituency() == constituency)
                return *members[i];
        }
        return PartyMember();
    }

    void join_party(PartyMember m)
    {
        members[member_count] = new PartyMember(m);
        members[member_count]->setID(validID);
        validID++;
        member_count++;
    }

    void join_party(PoliticalParty &pp)
    {
        for (int i = 0; i < pp.member_count; i++)
        {
            PartyMember *p = new PartyMember(*pp.members[i]);
            p->setID(validID++);
            p->setConstituency("");
            members[member_count++] = p;
        }
        for (int i = 0; i < pp.member_count; i++)
            delete pp.members[i];
        pp.member_count = 0;
    }

    void leave_party(int id)
    {

        int idx = -1;
        for (int i = 0; i < member_count; i++)
        {
            if (members[i]->getID() == id)
            {
                idx = i;
                break;
            }
        }
        delete members[idx];
        for (int i = idx; i < member_count - 1; i++)
            members[i] = members[i + 1];
        member_count--;
        members[member_count] = NULL;
    }

    void nominate_member(int id, string constituency)
    {

        int idx = -1;
        for (int i = 0; i < member_count; i++)
        {
            if (members[i]->getID() == id)
            {
                idx = i;
                break;
            }
        }
        members[idx]->setConstituency(constituency);
    }

    void show_nominated_members()
    {

        cout << "Nominated members of " << name << ":" << endl;
        for (int i = 0; i < member_count; i++)
            if (members[i]->getConstituency() != "")
                members[i]->show_member_details();
    }

    void cancel_nomination(int id)
    {

        int idx = -1;
        for (int i = 0; i < member_count; i++)
        {
            if (members[i]->getID() == id)
            {
                idx = i;
                break;
            }
        }
        members[idx]->setConstituency("");
    }

    PoliticalParty form_new_party(string name)
    {
        PoliticalParty new_party(name);
        for (int i = 0; i < member_count; i++)
        {
            if (members[i]->getConstituency() == "")
            {
                new_party.members[new_party.member_count] = members[i];
                new_party.members[new_party.member_count]->setID(new_party.validID++);
                new_party.member_count++;

                for (int j = i; j < member_count - 1; j++)
                {
                    members[j] = members[j + 1];
                }
                member_count--;
                i--;
            }
        }
        return new_party;
    }

    void show_all_members()
    {

        cout << "Members of " << name << ":" << endl;
        if (member_count == 0)
        {
            cout << "No members found." << endl;
            return;
        }
        for (int i = 0; i < member_count; i++)
            members[i]->show_member_details();
    }
};

int main()
{
    PartyMember abc1("Mr. A", 100000000);
    PartyMember abc2("Mr. B", 4000000);
    PartyMember abc3("Mr. C", 20000000);
    PoliticalParty p1("ABC");
    p1.join_party(abc1);
    p1.join_party(abc2);
    p1.join_party(abc3);
    p1.show_all_members();

    p1.nominate_member(1, "DHK-10");
    PartyMember pm = p1.search_member("DHK-10");
    cout << endl
         << "Details of the member nominated for DHK-10 constituency:" << endl;
    pm.show_member_details();

    p1.nominate_member(2, "CUM-3");
    p1.nominate_member(3, "SYL-1");
    p1.show_nominated_members();

    PartyMember xyz1("Mr. X", 1000000);
    PartyMember xyz2("Mr. Y", 3000000);

    PoliticalParty p2("XYZ");
    p2.join_party(xyz1);
    p2.join_party(xyz2);
    p2.show_all_members();

    p1.join_party(p2);
    p1.show_all_members();
    p2.show_all_members();

    PartyMember xyz3("Mr. Z", 5000000);
    p2.join_party(xyz3);
    p2.show_all_members();

    p1.cancel_nomination(1);
    p1.cancel_nomination(3);
    p1.nominate_member(4, "CUM-3");
    p1.nominate_member(5, "SYL-1");
    p1.show_nominated_members();

    PoliticalParty p3 = p1.form_new_party("Renegades");
    p1.show_all_members();
    p3.show_all_members();
}
